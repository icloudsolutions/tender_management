from odoo import fields, models


class ResConfigSettings(models.TransientModel):
    """Inherit res.config.settings to add auto-create template setting."""
    _inherit = 'res.config.settings'

    auto_create_template_from_crm_stage = fields.Boolean(
        string='Auto-create Project Template from CRM Stage',
        config_parameter='project_tasks_from_templates.auto_create_template_from_crm_stage',
        help='Enable this to automatically create project templates when CRM stages are created or updated.')
